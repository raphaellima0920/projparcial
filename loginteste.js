



btn.addEventListener('click', ()=>{
  let inputSenha = document.querySelector('#senha')
  
  if(inputSenha.getAttribute('type') == 'password'){
    inputSenha.setAttribute('type', 'text')
  } else {
    inputSenha.setAttribute('type', 'password')
  }
})

function entrar(){
  let login = document.querySelector('#login')
  let usuarioLabel = document.querySelector('#usuarioLabel')
  
  let senha = document.querySelector('#senha')
  let senhaLabel = document.querySelector('#senhaLabel')
  
  let msgError = document.querySelector('#msgError')
  let listaUser = []
  
  let userValid = {
    nome: null,
    user: null,
    senha: null
  }
  
  listaUser = JSON.parse(localStorage.getItem('listaUser'))
  
  listaUser?.forEach((item) => {
    if(login.value == item.userCad && senha.value == item.senhaCad){
       
      userValid = {
         nome: item.nomeCad,
         user: item.userCad,
         senha: item.senhaCad
       }
      
    }
  })
   
  if(login.value == userValid.user && senha.value == userValid.senha){
    setTimeout(()=>{
        window.location.href = 'logindeteste.html'
    }, 2000)
    
    let mathRandom = Math.random().toString(16).substr(2)
    let token = mathRandom + mathRandom
    
    localStorage.setItem('token', token)
    localStorage.setItem('userLogado', JSON.stringify(userValid))
  } else {
    usuarioLabel.setAttribute('style', 'color: red')
    login.setAttribute('style', 'border-color: red')
    senhaLabel.setAttribute('style', 'color: red')
    senha.setAttribute('style', 'border-color: red')
    msgError.setAttribute('style', 'display: block')
    msgError.innerHTML = 'Usu√°rio ou senha incorretos'
    login.focus()
  }
  
}